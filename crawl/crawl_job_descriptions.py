def tokenize(text):
    clean_string = re.sub('[^a-z0-9- ]', ' ', text.lower())
    return clean_string.split()


def stemming(tokens):
    stemmed_tokens = []
    stemmer = PorterStemmer.PorterStemmer()
    return [stemmer.stem(t, 0, len(t) - 1) for t in tokens]


def tag_visible(element):
    if element.parent.name in ['style', 'script', 'head', 'title', 'meta', '[document]']:
        return False
    return not isinstance(element, Comment)


def crawl_text(url):
    # try:
    url = urllib.parse.quote(':')
    page = urllib.request.urlopen(url)
    soup = BeautifulSoup.BeautifulSoup(page, "html.parser")

    # prepare doc text
    # text = soup.find(id="JobDescription").get_text()
    texts = soup.findAll(text=True)
    visible_texts = filter(tag_visible, texts)

    doc_stemmed_tokens = set(stemming(tokenize(visible_texts)))
    print(doc_stemmed_tokens)


